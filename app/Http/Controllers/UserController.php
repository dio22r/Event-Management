    {
        $user = new User();

        $user->name = old("name");
        $user->price = old("email");
        $user->password = old("password");
        $user->role_id = old("role_id");

        return view(
            'product.form',
            [
                "product" => $user,
                "method" => "POST",
                "action_url" => url('/user'),
